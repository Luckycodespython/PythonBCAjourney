# Project: Smart Arithmetic Calculator 

a = int(input("Enter your first number 'a': "))
b = int(input("Enter your second number 'b': "))

print("\n--- Results ---")
print("1. Subtraction (-):", a - b)
print("2. Addition (+):", a + b)
print("3. Multiplication (*):", a * b)
print("4. Division (/):", a / b)
print("5. Exponential (**):", a ** b)
print("6. Floor Division (//):", a // b)
print("7. Modulus (%):", a % b)


